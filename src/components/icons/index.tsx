import { type ReactNode } from 'react'

interface IconProps {
  size?: number
  color?: string
  strokeWidth?: number
}

function Svg({ size = 20, color = 'currentColor', strokeWidth = 1.5, children }: IconProps & { children: ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke={color}
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      {children}
    </svg>
  )
}

// Filled variant for brand glyphs (Instagram, X, etc.), whose marks read best
// as solid shapes rather than the line-art used elsewhere. Shares the same
// 24×24 viewBox and currentColor handling as Svg.
function BrandSvg({ size = 20, color = 'currentColor', children }: IconProps & { children: ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={color}
      aria-hidden="true"
    >
      {children}
    </svg>
  )
}

